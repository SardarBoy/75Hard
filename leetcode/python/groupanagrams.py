class Solution:
    def groupAnagrams(self, strs: List[str]) -> List[List[str]]:
        dic=defaultdict(list)
        for s in strs:
            alphabets=[0]*26
            for c in s:
                alphabets[ord(c)-ord('a')]+=1
            dic[tuple(alphabets)].append(s)
            print(alphabets)
        return list(dic.values())
            
